def suma_kvadrativ(n: int) -> int:
    """Повертає суму квадратів чисел від 1 до n включно.

    Якщо n < 1 — повертає 0.
    Формула: n(n+1)(2n+1)/6. Результат цілий без float-похибок.
    """
    if n < 1:
        return 0
    return n * (n + 1) * (2 * n + 1) // 6


