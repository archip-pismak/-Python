# Модуль містить реалізацію функції суми квадратів 1..N та просту взаємодію з користувачем

def suma_kvadrativ(n: int) -> int:
    """Повертає суму квадратів чисел від 1 до n включно.

    Якщо n < 1 — повертає 0.
    Формула: n(n+1)(2n+1)/6. Результат цілий (без float-похибок).
    """
    # Граничний випадок: для n < 1 сума порожня і дорівнює 0
    if n < 1:
        return 0
    # Закрита формула: S = n(n+1)(2n+1)/6, використовуємо цілочисельне ділення
    return n * (n + 1) * (2 * n + 1) // 6


def _read_nat_int(prompt: str) -> int:
    """Зчитує натуральне число (>= 1) з консолі з валідацією."""
    while True:
        try:
            value = int(input(prompt))
            if value < 1:
                print("N має бути натуральним числом (>= 1). Спробуйте ще раз.")
                continue
            return value
        except ValueError:
            print("Помилка: введіть ціле число. Спробуйте ще раз.")

if __name__ == "__main__":
    # Проста взаємодія: користувач вводить N, програма виводить суму квадратів 1..N
    print("Сума квадратів чисел від 1 до N")
    n = _read_nat_int("Введіть N: ")
    # Виклик функції з цього модуля
    s = suma_kvadrativ(n)
    print(f"S(1..{n}) = {s}")
