#Знайти та роздрукувати середнє арифметичне квадратів усіх цілих чисел від a до b. Прийняти a=5, b=80.

# Задано межі
a = 5
b = 80

# Знаходимо квадрати чисел і обчислюємо середнє
suma = 0
count = 0

for i in range(a, b + 1):
    suma += i**2
    count += 1

average = suma / count

print("Середнє арифметичне квадратів чисел від", a, "до", b, "дорівнює:", average)
