# Вводиться ціле число N (1<N<9), а виводяться рядки з числами або іншими символами (*, #), які утворюють визначений «рисунок» (останній задається варіантом).

def main():
    try:
        n = int(input("Введіть ціле число N (1 < N < 9): ").strip())
    except ValueError:
        print("Помилка: потрібно ввести ціле число.")
        return

    if not (1 < n < 9):
        print("Помилка: N має задовольняти умову 1 < N < 9.")
        return

    # Друк трикутника чисел
    for i in range(1, n + 1):
        рядок = " ".join(str(k) for k in range(1, i + 1))
        print(рядок)


if __name__ == "__main__":
    main()